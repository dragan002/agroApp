    <h2>Ko smo mi</h2>
    <p>AgroApp je digitalna tržnica koja povezuje farmere iz Prnjavora i okoline sa kupcima. Aplikacija je dostupna na adresi <a href="https://agroapp-production-54a0.up.railway.app">agroapp-production-54a0.up.railway.app</a>.</p>

    <h2>Koje podatke prikupljamo</h2>
    <p><strong>Farmeri (registrovani korisnici):</strong> Ime, e-mail adresa, lozinka (kriptovana), broj telefona, Viber i WhatsApp kontakt, fotografije farme i proizvoda, lokacija (grad/adresa).</p>
    <p><strong>Kupci (gosti):</strong> Ne tražimo registraciju. Ako ostavite recenziju, vaša IP adresa se čuva kao SHA-256 hash (nije čitljiva) isključivo radi sprečavanja zloupotrebe.</p>

    <h2>Kako koristimo podatke</h2>
    <ul>
        <li>Prikaz profila farmera i proizvoda kupcima</li>
        <li>Omogućavanje direktnog kontakta (poziv, Viber, WhatsApp)</li>
        <li>Ograničavanje broja recenzija po IP adresi (jedna recenzija po farmeru na 24h)</li>
    </ul>

    <h2>Dijeljenje podataka</h2>
    <p>Vaše podatke ne prodajemo niti dijelimo sa trećim stranama. Kontaktni podaci farmera (telefon, Viber, WhatsApp) su vidljivi svim posjetiocima aplikacije, jer je svrha aplikacije upravo direktan kontakt.</p>

    <h2>Fotografije</h2>
    <p>Fotografije koje farmeri učitaju čuvaju se na Cloudflare R2 servisu i javno su dostupne u okviru aplikacije.</p>

    <h2>Sigurnost</h2>
    <p>Lozinke se čuvaju u kriptovanom obliku (bcrypt). Komunikacija sa aplikacijom je zaštićena HTTPS protokolom.</p>

